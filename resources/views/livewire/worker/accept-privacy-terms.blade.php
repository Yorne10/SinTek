            <script>
                document.addEventListener('DOMContentLoaded', function () {
                    const swalWithBootstrapButtons = Swal.mixin({
                        customClass: {
                            confirmButton: 'btn btn-success me-2',
                            cancelButton: 'btn btn-danger'
                        },
                        buttonsStyling: false
                    });

                    // Continue button click handler
                    document.getElementById('continueBtn').addEventListener('click', function () {
                        swalWithBootstrapButtons.fire({
                            title: 'Términos de Privacidad',
                            html: '<p class="mb-2">¿Aceptas los términos y condiciones del aviso de privacidad?</p>' +
                                '<p class="small text-muted">Si no aceptas se cerrará tu sesión.</p>',
                            icon: 'question',
                            showCancelButton: true,
                            confirmButtonText: 'Sí, aceptar',
                            cancelButtonText: 'Rechazar',
                            reverseButtons: true,
                            allowOutsideClick: false,
                            allowEscapeKey: false
                        }).then((result) => {
                            if (result.isConfirmed) {
                                // Accept terms
                                @this.call('acceptTerms');
                            } else if (result.dismiss === Swal.DismissReason.cancel) {
                                // Reject terms
                                swalWithBootstrapButtons.fire({
                                    title: 'Términos rechazados',
                                    text: 'Tu sesión será cerrada. Debes aceptar los términos para usar el sistema.',
                                    icon: 'warning',
                                    confirmButtonText: 'Entendido',
                                    allowOutsideClick: false
                                }).then(() => {
                                    @this.call('rejectTerms');
                                });
                            }
                        });
                    });

                    // Listen for acceptance success
                    if (window.Livewire) {
                        Livewire.on('terms-accepted', (event) => {
                            const detail = event || {};
                            const redirectUrl = detail.redirectUrl || '/p/sintek/dashboard';
                            swalWithBootstrapButtons.fire({
                                icon: 'success',
                                title: '¡Términos aceptados!',
                                text: 'Serás redirigido al sistema.',
                                confirmButtonText: 'Aceptar'
                            }).then(() => {
                                window.location.href = redirectUrl;
                            });
                        });

                        Livewire.on('terms-error', (event) => {
                            const detail = event || {};
                            swalWithBootstrapButtons.fire({
                                icon: 'error',
                                title: detail.title || 'Error',
                                text: detail.message || '',
                                confirmButtonText: 'Aceptar'
                            });
                        });
                    }
                });
            </script>
